public class Car
{
    private String colour;
    private int passengerCount;
    
    public Car(string colour, int passengerCount)
    {
        this.colour = colour;
        this.passengerCount = passengerCount;
    }

    public string GetColor()
    {
        return colour;
    }

    public int GetPassengerCount()
    {
        return passengerCount;
    }
}
